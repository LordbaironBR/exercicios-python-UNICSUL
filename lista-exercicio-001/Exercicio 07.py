# desenvolva um programa em python para inserir comprimento e largura de um retangulo e encontre su área
# FORMULA: (base . altura)/2

h = float(input("Digite a áltura do triangulo (centimetros): "))
b = float(input("Digite a largura do triangulo (centimetros): "))
fomula = (b*h)/2

print("A área do triangulo de base {:.0f} e altura {:.0f} é igual a: {:.0f} centimetros".format(b,h,fomula)
)
