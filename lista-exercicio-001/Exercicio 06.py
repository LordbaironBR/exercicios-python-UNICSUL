# Desenvolva um programa para inserir comprimento e largura de um retanfulo e encontre sua area
# Formula:  base.altura

h = float(input("Digite a áltura do retangulo (metros): "))
b = float(input("Digite a largura do retangulo (metros): "))
fomula = b * h

print("A área do retangulo de base {:.0f} e altura {:.0f} é igual a: {:.0f} metros".format(b,h,fomula)
)
